package br.senai.sp.model;

public class Posicao {
	
	public String xpos ;
	public String ypos ;
	public String zpos ;
	public String rpos ;
	public int tempo ;
	
	public Posicao(String xpos, String ypos, String zpos, String rpos, int tempo) {
		super();
		this.xpos = xpos;
		this.ypos = ypos;
		this.zpos = zpos;
		this.rpos = rpos;
		this.tempo = tempo;
	}





}
